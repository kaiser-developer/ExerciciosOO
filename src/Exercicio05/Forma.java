package Exercicio05;

public class Forma {
    public String escreveCom;
    public String ehFeitoDe;

    public Forma(String escreveCom, String ehFeitoDe) {
        this.escreveCom = escreveCom;
        this.ehFeitoDe = ehFeitoDe;
    }

    public String getEscreveCom() {
        return escreveCom;
    }

    public String getEhFeitoDe() {
        return ehFeitoDe;
    }
}
